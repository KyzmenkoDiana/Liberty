    <h4><img src="img/срИзг.jpg" width="500" height="400" alt="Иллюстрация" align="left" vspace="5" hspace="15">
    Можно ли быстро изготовить индивидуальный худи на заказ из премиум тканей, с контролем качества на каждом этапе производства и по акционной цене от 2990 рублей? Команда Hooli совершенствует процесс работы, чтобы ты и твои близкие смогли носить худи уже через 2 недели после заказа!</h4>
    <p>В настоящее время пошив худи или свитшота занимает примерно 6-10 дней. 85% изделий отшивается в этот срок. Доступна услуга ускоренного пошива, когда изделие будет готово в течение 3-5 дней (ххх рублей).</p> 
    <p>Мы стараемся сделать все заказы максимально быстро, но в связи с нюансами индивидуального пошива, мы не всегда можем гарантировать, что наши мастера успеют отшить изделия к конкретной дате, например к свадьбе (да, и такое было: мы правда в этот раз успели, но лучше не рисковать) или другому важному мероприятию. Лучше позаботиться об этом заблаговременно. Если тебе нужно 100% получить заказ в ближайшие 2-3 дня, обратись к менеджерам - они помогут подобрать готовую одежду из наличия.</p>
<br>
    <p>Если ты ждешь худи в эксклюзивной расцветке или с авторским принтом - это увеличит срок изготовления. Если ты заказал (-а) комплект изделий, например костюм худи с джоггерами, мы отправим его после готовности всего заказа.</p>
<p>По ряду причин срок изготовления может быть увеличен. Даже в случае выявления незначительных недочетов, отдел качества отправляет изделие на доработку.</p>
<p>Ты всегда можешь самостоятельно следить за ходом изготовления твоего заказа .</p>
